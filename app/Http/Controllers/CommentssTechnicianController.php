     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        if (Auth::check())
        {
            $ticket = Ticket::where('slug', $request->ticket_slug)->first();
            if($ticket)
            {
                Comment::create([
                    'user_id' => Auth::user()->id,
                    'ticket_id' => $ticket->id,
                    'comment' => $request->comment
                ]);
                return redirect()->back();
            }
            else
            {
                return redirect()->back();
            }
        }
    }

